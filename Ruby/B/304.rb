n = gets.to_i

if n <= (10**3)-1
  p n
elsif 10**3 <= n && n <= (10**4)-1
  p (n/10)*10
elsif 10**4 <= n && n <= (10**5)-1
  p (n/100)*100
elsif 10**5 <= n && n <= (10**6)-1
  p (n/1000)*1000
elsif 10**6 <= n && n <= (10**7)-1
  p (n/10_000)*10_000
elsif 10**7 <= n && n <= (10**8)-1
  p (n/100_000)*100_000
elsif 10**8 <= n && n <= (10**9)-1
  p (n/1_000_000)*1_000_000
end
